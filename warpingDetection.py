def calculateCorners(rects):
    left_x = np.array([rect[0] for rect in rects])
    upper_y = np.array([rect[1] for rect in rects])
    right_x = np.array([rect[0] + rect[2] for rect in rects])
    lower_y = np.array([rect[1] + rect[3] for rect in rects])
    max_x = left_x.max()
    max_y = lower_y.max()

    upper_left_cond = left_x + upper_y
    #print 'upper_left_cond', upper_left_cond, 'left_x', left_x, 'upper_y', upper_y
    upper_left_index = upper_left_cond.argmin()
    #print 'upper_left_index', upper_left_index

    lower_right_cond = right_x + lower_y
    lower_right_index = lower_right_cond.argmax()

    upper_right_cond = max_x - right_x + max_y + upper_y
    upper_right_index = upper_right_cond.argmin()

    lower_left_cond = max_x - left_x + lower_y
    lower_left_index = lower_left_cond.argmax()

#    print('upper_left :', upper_left_index)
#    print('lower_right:', lower_right_index)
#    print('upper_right:', upper_right_index)
#    print('lower_left :', lower_left_index)
    return upper_left_index, lower_right_index, upper_right_index, lower_left_index

def filterNeighbours(rect, medianDimension, r1):
    res = 0
    if (abs(rect[X] - r1[X]) < medianDimension*DIMENSION_FACTOR_MAX*ALONE_DISTANCE_FACTOR
                            and abs(rect[Y] - r1[Y]) < medianDimension*DIMENSION_FACTOR_MAX*ALONE_DISTANCE_FACTOR
                            and abs(rect[X] - r1[X]) > medianDimension*DIMENSION_FACTOR_MAX
                            and abs(rect[Y] - r1[Y]) > medianDimension*DIMENSION_FACTOR_MAX):
        res = 1
    return res

def filterSingleRectangles(r, medianDimension, rects):
    res = True
