def checkRectangleSize(r, medianDimension):
    res = True
    relation = r[H]/r[W]
    if r[W] > r[H]:
        relation = r[W]/r[H]
    if relation > MAX_ALLOWED_RELATION_WIDTH_TO_HEIGHT:
        res = False
    if r[W] < medianDimension*DIMENSION_FACTOR_MIN or r[W] > medianDimension*DIMENSION_FACTOR_MAX:
        res = False
    if r[H] < medianDimension*DIMENSION_FACTOR_MIN or r[H] > medianDimension*DIMENSION_FACTOR_MAX:
        res = False
    return res

# check whether has neighbours
def checkNeighbours(r, medianDimension, rectangles):
    res = True
    neighbours = filter(lambda r1: abs(r[X] - r1[X]) < medianDimension*DIMENSION_FACTOR_MAX
                        and abs(r[Y] - r1[Y]) < medianDimension*DIMENSION_FACTOR_MAX, rectangles)
    if len(neighbours) < MIN_NEIGHBOURS:
        res = False
    return res


# check whether rectangle is alone
def checkAlone(r, medianDimension, rectangles):
    res = True
    neighbours = filter(lambda r1: abs(r[X] - r1[X]) < medianDimension*DIMENSION_FACTOR_MAX*ALONE_DISTANCE_FACTOR
                        and abs(r[Y] - r1[Y]) < medianDimension*DIMENSION_FACTOR_MAX*ALONE_DISTANCE_FACTOR
                        and abs(r[X] - r1[X]) > medianDimension*DIMENSION_FACTOR_MAX
                        and abs(r[Y] - r1[Y]) > medianDimension*DIMENSION_FACTOR_MAX, rectangles)
    if len(neighbours) < SOME_NEIGHBOURS:
        res = False
    return res

