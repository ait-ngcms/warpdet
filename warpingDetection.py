# Name:        warpingDetection.py
# Purpose:     This file supports analysis of digital collection (e.g. JPG files)
#              for warping and cropping problems during the automated scan process.
#              In order to detect warping or cropping problems we involve six use cases:
#                 1. Text is shifted to the one side of the image and other border
#                    is much wider than first rand.
#                 2. There is no gap between text and image border - text is cut
#                    by the rand.
#                 3. Image comprises part of the text from previous page.
#                 4. Text page is rotated.
#                 5. Text page is warped optically.
#                 6. Text page is warped physically.
#
# Author:      Roman Graf
#
